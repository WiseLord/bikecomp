#ifndef PINS_H
#define PINS_H

#include <avr/io.h>
#include "arduinopromini.h"

#define CONCAT(x,y)         x ## y

#define DDR(x)              CONCAT(DDR,x)
#define PORT(x)             CONCAT(PORT,x)
#define PIN(x)              CONCAT(PIN,x)

// LCD non-SPI pins
#define ILI9341_RST         ARDUINO_A2
#define ILI9341_RST_LINE    ARDUINO_A2_LINE
#define ILI9341_DC          ARDUINO_A1
#define ILI9341_DC_LINE     ARDUINO_A1_LINE
#define ILI9341_LED         ARDUINO_A0
#define ILI9341_LED_LINE    ARDUINO_A0_LINE

// LCD SPI pins
#define ILI9341_SDI         ARDUINO_MOSI
#define ILI9341_SDI_LINE    ARDUINO_MOSI_LINE
#define ILI9341_SCK         ARDUINO_SCLK
#define ILI9341_SCK_LINE    ARDUINO_SCLK_LINE
#define ILI9341_SDO         ARDUINO_MISO
#define ILI9341_SDO_LINE    ARDUINO_MISO_LINE
#define ILI9341_CS          ARDUINO_SS
#define ILI9341_CS_LINE     ARDUINO_SS_LINE


#define ILI9341_CS_SET      PORT(ILI9341_CS) |= ILI9341_CS_LINE
#define ILI9341_CS_RESET    PORT(ILI9341_CS) &= ~ILI9341_CS_LINE
#define ILI9341_RST_SET     PORT(ILI9341_RST) |= ILI9341_RST_LINE
#define ILI9341_RST_RESET   PORT(ILI9341_RST) &= ~ILI9341_RST_LINE
#define ILI9341_DC_SET      PORT(ILI9341_DC) |= ILI9341_DC_LINE
#define ILI9341_DC_RESET    PORT(ILI9341_DC) &= ~ILI9341_DC_LINE
#define ILI9341_LED_SET     PORT(ILI9341_LED) |= ILI9341_LED_LINE
#define ILI9341_LED_RESET   PORT(ILI9341_LED) &= ~ILI9341_LED_LINE
#define ILI9341_SDI_SET     PORT(ILI9341_SDI) |= ILI9341_SDI_LINE
#define ILI9341_SDI_RESET   PORT(ILI9341_SDI) &= ~ILI9341_SDI_LINE
#define ILI9341_SCK_SET     PORT(ILI9341_SCK) |= ILI9341_SCK_LINE
#define ILI9341_SCK_RESET   PORT(ILI9341_SCK) &= ~ILI9341_SCK_LINE
#define ILI9341_SDO_SET     PORT(ILI9341_SDO) |= ILI9341_SDO_LINE
#define ILI9341_SDO_RESET   PORT(ILI9341_SDO) &= ~ILI9341_SDO_LINE

#define ILI9341_RST_OUT     DDR(ILI9341_RST) |= ILI9341_RST_LINE
#define ILI9341_DC_OUT      DDR(ILI9341_DC) |= ILI9341_DC_LINE
#define ILI9341_LED_OUT     DDR(ILI9341_LED) |= ILI9341_LED_LINE

#define ILI9341_SDI_OUT     DDR(ILI9341_SDI) |= ILI9341_SDI_LINE
#define ILI9341_SCK_OUT     DDR(ILI9341_SCK) |= ILI9341_SCK_LINE
#define ILI9341_CS_OUT      DDR(ILI9341_CS) |= ILI9341_CS_LINE

#endif // PINS_H
